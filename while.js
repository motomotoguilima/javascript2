//iniciando o contador de vezes que ira executar com IF
var contador = 1
if(contador <=8){
    console.log(`comer fatia ${contador}`)
    contador++ //contador +=1 || contador=contador+1
}
console.log (`fim da execução`)



//iniciando o contador de vezes que ira executar
var contador = 1
while(contador <=8){
    console.log(`comer fatia ${contador}`)
    contador++ //contador +=1 || contador=contador+1
}
console.log (`fim da execução`)


//iniciando contador com for
